--step 1 : creating a table for the  customer  details


CREATE TABLE BANK_CUST
   (	CUST_ID NUMBER(6,0) , 
	CUST_FIRST_NAME VARCHAR2(25), 
	INITIALS VARCHAR2(1),
	CUST_LAST_NAME VARCHAR2(20), 
	CUST_GENDER VARCHAR2(1), 
	CUST_DOB DATE, 
	CUST_TYPE VARCHAR2(4)
   ) ;



--step 2 :creating a table for employee


CREATE TABLE BANK_EMPLOYEE 
   (	BANK_EMP_ID NUMBER(5,0), 
	EMP_NAME VARCHAR2(40), 
	EMP_GENDER CHAR(1), 
	DESIGNATION VARCHAR2(20), 
	MANAGER_ID NUMBER(6,0)
   ) ;



--step 3 : creating a table of customer for contact details


CREATE TABLE BANK_CUST_CONTACT 
   (	CUST_ID NUMBER(6), 
	CUST_PHONE NUMBER, 
	CONTACT_TYPE VARCHAR2(10), 
	CUST_ADDR_LINE1 VARCHAR2(30), 
	CUST_ADDR_LINE2 VARCHAR2(30), 
	CUST_CITY VARCHAR2(25), 
	CUST_STATE VARCHAR2(25), 
	CUST_PIN NUMBER(6,0)
   ) ;


--step 4 : creating a table for fixed depoist


CREATE TABLE BANK_FD_ACCOUNT 
   (	ACCOUNT_NO NUMBER(15,0), 
	CUST_ID NUMBER(6,0), 
	START_DT DATE, 
	FD_TERM_MNTH NUMBER(3,0), 
	INITIAL_AMT NUMBER(20,2), 
	ACC_STATUS VARCHAR2(10) DEFAULT 'Active'
   ) ;



--step 5 : creating a table for recurring deposit


  CREATE TABLE BANK_RD_ACCOUNT 
   (	ACCOUNT_NO NUMBER(15,0), 
	RD_CUST_ID NUMBER(6,0), 
	RD_START_DT DATE, 
	RD_TERM_YRS NUMBER(3,0), 
	INSTALLMENT_AMT NUMBER(20,2), 
	CURR_BALANCE NUMBER(20,2), 
	ACC_STATUS VARCHAR2(10)
   ) ;



--step 6 : creating a table for interest


  CREATE TABLE BANK_INT_RATE 
   (	ACCOUNT_TYPE_CD VARCHAR2(4), 
	INTEREST_RATE NUMBER(5,2), 
	MIN_AMT NUMBER(20,2)
   ) ;


--step 7 : creating a table for Transaction


  CREATE TABLE BANK_TRANSACTION 
   (	TRANS_NO NUMBER(16,0), 
	TRANS_TYPE CHAR(2), 
	TRANS_DT DATE, 
	TRANS_ACC_NO NUMBER(15,0), 
	TRANS_DESC VARCHAR2(50), 
	TRANS_AMT NUMBER(20,2), 
	BANK_EMP_ID NUMBER(5,0)
   ) ;




--step 8 : creating a table for savings 


  CREATE TABLE BANK_SB_ACCOUNT 
   (	ACCOUNT_NO NUMBER(15,0), 
	PRIMARY_CUST_ID NUMBER(6,0), 
	SECONDARY_CUST_ID NUMBER(6,0), 
	CURR_BAL_AMT NUMBER(20,2), 
	ACC_STATUS VARCHAR2(10) DEFAULT 'Active', 
	START_DATE DATE, 
	END_DATE DATE
   ) ;

